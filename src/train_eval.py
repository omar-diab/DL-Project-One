import random
import numpy as np
import torch
import torch.nn as nn
import torch.optim as optim

from src.model import AblationMLP
from src.early_stopping import EarlyStopping

# Set all random seeds for reproducibility
def set_seed(seed: int = 42):
    random.seed(seed)
    np.random.seed(seed)
    torch.manual_seed(seed)
    if torch.cuda.is_available():
        torch.cuda.manual_seed_all(seed)

# Select GPU if available, otherwise use CPU
def get_device():
    return torch.device("cuda" if torch.cuda.is_available() else "cpu")

# Compute classification accuracy on a given dataloader
def evaluate_accuracy(model, dataloader, device):
    model.eval()
    correct = 0
    total = 0

    with torch.no_grad():
        for images, labels in dataloader:
            images = images.to(device)
            labels = labels.to(device)

            outputs = model(images)
            preds = outputs.argmax(dim=1)

            total += labels.size(0)
            correct += (preds == labels).sum().item()

    return 100.0 * correct / total

# Compute average loss on a given dataloader
def evaluate_loss(model, dataloader, loss_fn, device):
    model.eval()
    total_loss = 0.0

    with torch.no_grad():
        for images, labels in dataloader:
            images = images.to(device)
            labels = labels.to(device)

            outputs = model(images)
            loss = loss_fn(outputs, labels)
            total_loss += loss.item()

    return total_loss / len(dataloader)

# Compute the explicit L2 penalty over trainable weight parameters
def l2_penalty(model):
    penalty = 0.0
    for name, param in model.named_parameters():
        if param.requires_grad and "weight" in name:
            penalty = penalty + torch.sum(param ** 2)
    return penalty

# Train the model for one epoch, with optional explicit L2 regularization
def train_one_epoch(
    model,
    dataloader,
    optimizer,
    loss_fn,
    device,
    l2_lambda: float = 0.0,
    use_explicit_l2: bool = False
):
    model.train()
    running_loss = 0.0
    running_ce_loss = 0.0
    running_l2_term = 0.0

    for images, labels in dataloader:
        images = images.to(device)
        labels = labels.to(device)

        optimizer.zero_grad()
        outputs = model(images)

        ce_loss = loss_fn(outputs, labels)
        loss = ce_loss

        l2_term = torch.tensor(0.0, device=device)
        if use_explicit_l2 and l2_lambda > 0.0:
            l2_term = l2_lambda * l2_penalty(model)
            loss = ce_loss + l2_term

        loss.backward()
        optimizer.step()

        running_loss += loss.item()
        running_ce_loss += ce_loss.item()
        running_l2_term += l2_term.item()

    return {
        "total_loss": running_loss / len(dataloader),
        "ce_loss": running_ce_loss / len(dataloader),
        "l2_term": running_l2_term / len(dataloader)
    }

# Run a full experiment with a fixed configuration
def run_experiment(
    train_loader,
    val_loader,
    test_loader,
    experiment_name: str,
    hidden_sizes=None,
    use_dropout: bool = False,
    dropout_p: float = 0.5,
    use_batchnorm: bool = False,
    use_early_stopping: bool = False,
    patience: int = 5,
    min_delta: float = 1e-4,
    learning_rate: float = 0.01,
    weight_decay: float = 0.0,
    use_explicit_l2: bool = False,
    max_epochs: int = 50,
    seed: int = 42,
):
    # Set seed and choose device
    set_seed(seed)
    device = get_device()

    # Build the model for this experiment
    model = AblationMLP(
        hidden_sizes=hidden_sizes,
        use_dropout=use_dropout,
        dropout_p=dropout_p,
        use_batchnorm=use_batchnorm
    ).to(device)

    # Use built-in weight decay only when explicit L2 is not enabled
    optimizer = optim.SGD(
        model.parameters(),
        lr=learning_rate,
        weight_decay=0.0 if use_explicit_l2 else weight_decay
    )

    loss_fn = nn.CrossEntropyLoss()

    # Store training history for later plotting and comparison
    history = {
        "train_loss": [],
        "train_ce_loss": [],
        "train_l2_term": [],
        "val_loss": [],
        "test_loss": [],
        "train_acc": [],
        "val_acc": [],
        "test_acc": [],
    }

    # Create early stopping object if requested
    stopper = None
    if use_early_stopping:
        stopper = EarlyStopping(patience=patience, min_delta=min_delta)

    for epoch in range(1, max_epochs + 1):
        # Train for one epoch
        train_stats = train_one_epoch(
            model=model,
            dataloader=train_loader,
            optimizer=optimizer,
            loss_fn=loss_fn,
            device=device,
            l2_lambda=weight_decay,
            use_explicit_l2=use_explicit_l2
        )

        train_loss = train_stats["total_loss"]
        train_ce_loss = train_stats["ce_loss"]
        train_l2_term = train_stats["l2_term"]

        # Evaluate on validation and test sets
        val_loss = evaluate_loss(model, val_loader, loss_fn, device)
        test_loss = evaluate_loss(model, test_loader, loss_fn, device)

        train_acc = evaluate_accuracy(model, train_loader, device)
        val_acc = evaluate_accuracy(model, val_loader, device)
        test_acc = evaluate_accuracy(model, test_loader, device)

        # Save metrics
        history["train_loss"].append(train_loss)
        history["train_ce_loss"].append(train_ce_loss)
        history["train_l2_term"].append(train_l2_term)
        history["val_loss"].append(val_loss)
        history["test_loss"].append(test_loss)
        history["train_acc"].append(train_acc)
        history["val_acc"].append(val_acc)
        history["test_acc"].append(test_acc)

        # Print progress for the current epoch
        print(
            f"{experiment_name} | Epoch {epoch}/{max_epochs} | "
            f"Train Loss: {train_loss:.4f} | CE: {train_ce_loss:.4f} | L2: {train_l2_term:.4f} | "
            f"Val Loss: {val_loss:.4f} | Test Loss: {test_loss:.4f} | "
            f"Train Acc: {train_acc:.2f}% | Val Acc: {val_acc:.2f}% | Test Acc: {test_acc:.2f}%"
        )

        # Apply early stopping based on validation loss
        if stopper is not None:
            should_stop = stopper.step(val_loss, model, epoch)
            if should_stop:
                print(f"Early stopping triggered at epoch {epoch}. Restoring best epoch {stopper.best_epoch}.")
                model.load_state_dict(stopper.best_state)
                break

    # Compute final metrics after training finishes
    final_train_loss = evaluate_loss(model, train_loader, loss_fn, device)
    final_val_loss = evaluate_loss(model, val_loader, loss_fn, device)
    final_test_loss = evaluate_loss(model, test_loader, loss_fn, device)

    final_train_acc = evaluate_accuracy(model, train_loader, device)
    final_val_acc = evaluate_accuracy(model, val_loader, device)
    final_test_acc = evaluate_accuracy(model, test_loader, device)

    # Package all results in a dictionary
    result = {
        "name": experiment_name,
        "model": model,
        "history": history,
        "final_train_loss": final_train_loss,
        "final_val_loss": final_val_loss,
        "final_test_loss": final_test_loss,
        "final_train_acc": final_train_acc,
        "final_val_acc": final_val_acc,
        "final_test_acc": final_test_acc,
        "generalization_gap_val": final_train_acc - final_val_acc,
        "generalization_gap_test": final_train_acc - final_test_acc,
        "device": str(device),
        "used_explicit_l2": use_explicit_l2,
        "weight_decay_or_lambda": weight_decay,
    }

    # Add early stopping metadata if used
    if stopper is not None:
        result["best_epoch"] = stopper.best_epoch
        result["best_val_loss"] = stopper.best_loss

    return result
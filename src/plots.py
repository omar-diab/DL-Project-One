# Plot the generalization gap using train-validation and train-test differences
def plot_generalization_gap(result, save_path=None, show=True):
    history = result["history"]
    epochs = range(1, len(history["train_acc"]) + 1)

    gap_val = [
        train - val
        for train, val in zip(history["train_acc"], history["val_acc"])
    ]

    gap_test = [
        train - test
        for train, test in zip(history["train_acc"], history["test_acc"])
    ]

    plt.figure(figsize=(8, 5))
    plt.plot(epochs, gap_val, label="Train - Validation Gap")
    plt.plot(epochs, gap_test, label="Train - Test Gap")
    plt.xlabel("Epoch")
    plt.ylabel("Gap (%)")
    plt.title(f'{result["name"]} - Generalization Gap')
    plt.legend()
    plt.grid(True)
    plt.tight_layout()

    if save_path is not None:
        ensure_dir(os.path.dirname(save_path))
        plt.savefig(save_path, bbox_inches="tight")

    if show:
        plt.show()
    else:
        plt.close()

